/**************************************************************************
函数功能：单位时间读取编码器计数
入口参数：定时器
返回  值：速度值
**************************************************************************/
int Read_Encoder(u8 TIMX)
{
   int Encoder_TIM;    
   switch(TIMX)
	 {
		 case 1:  Encoder_TIM= (short)TIM_GetCounter(TIM1);   TIM_SetCounter(TIM1,0);   break;
	   case 2:  Encoder_TIM= (short)TIM2 -> CNT;    break;
		 default:  Encoder_TIM=0;
	 }

		return Encoder_TIM;
}

